    public void FillPageSP() {
        int vitri = 0;
        list = dao.selectAll();
        for (SanPham sp : list) {
            //System.out.println(vitri);
            vitri++;
            if (vitri == this.page * 12 - 11) {
                FillSP(sp, pnel1, hinh1, ten1, gia1, sl1, lblgia1, lblKho1);
            } else if (vitri == this.page * 12 - 10) {
                FillSP(sp, pnel2, hinh2, ten2, gia2, sl2, lblgia2, lblKho2);
            } else if (vitri == this.page * 12 - 9) {
                FillSP(sp, pnel3, hinh3, ten3, gia3, sl3, lblgia3, lblKho3);
            } else if (vitri == this.page * 12 - 8) {
                FillSP(sp, pnel4, hinh4, ten4, gia4, sl4, lblgia4, lblKho4);
            } else if (vitri == this.page * 12 - 7) {
                FillSP(sp, pnel5, hinh5, ten5, gia5, sl5, lblgia5, lblKho5);
            } else if (vitri == this.page * 12 - 6) {
                FillSP(sp, pnel6, hinh6, ten6, gia6, sl6, lblgia6, lblKho6);
            } else if (vitri == this.page * 12 - 5) {
                FillSP(sp, pnel7, hinh7, ten7, gia7, sl7, lblgia7, lblKho7);
            } else if (vitri == this.page * 12 - 4) {
                FillSP(sp, pnel8, hinh8, ten8, gia8, sl8, lblgia8, lblKho8);
            } else if (vitri == this.page * 12 - 3) {
                FillSP(sp, pnel9, hinh9, ten9, gia9, sl9, lblgia9, lblKho9);
            } else if (vitri == this.page * 12 - 2) {
                FillSP(sp, pnel10, hinh10, ten10, gia10, sl10, lblgia10, lblKho10);
            } else if (vitri == this.page * 12 - 1) {
                FillSP(sp, pnel11, hinh11, ten11, gia11, sl11, lblgia11, lblKho11);
            } else if (vitri == this.page * 12) {
                FillSP(sp, pnel12, hinh12, ten12, gia12, sl12, lblgia12, lblKho12);
            }
        }
    }
    
