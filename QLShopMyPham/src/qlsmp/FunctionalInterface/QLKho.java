    public void FillSP(SanPham sp, JPanel pnel, JLabel hinh, JLabel ten, JLabel gia, JLabel sl, JLabel lblGia, JLabel lblKho) {
        try {
            setHinhAnh(sp.getHinh(), hinh);
            hinh.setBorder(BorderFactory.createLineBorder(new Color(204, 204, 204), 1));
            pnel.setBorder(BorderFactory.createLineBorder(new Color(204, 204, 204), 1));
            //pnel.setVisible(true);
            ten.setText(sp.getTensp());
            gia.setText(sp.getGia() + "");
            if (sp.getSoLuong() < 10 && sp.getSoLuong() > 0) {
                sl.setForeground(Color.red);
            } else if (sp.getSoLuong() == 0) {
                sl.setForeground(Color.red);
                sl.setText("Hết");
            } else {
                sl.setForeground(Color.black);
            }
            sl.setText(sp.getSoLuong() + "");
            lblGia.setText("Giá:");
            lblKho.setText("Kho còn:");
        } catch (Exception e) {
        }
    }
    
