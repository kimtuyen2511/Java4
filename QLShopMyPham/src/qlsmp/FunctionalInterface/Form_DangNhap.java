 public void dangNhap() {
        String manv = txtUsername.getText();
        String matKhau = new String(txtPass.getPassword());
        Account acc = dao.selecteByUsername(manv);
        if (manv.isBlank() || matKhau.isBlank()) {
            JOptionPane.showMessageDialog(this, "Thông tin chưa nhập đủ");
            return;
        }
        if (acc == null) {
            txtUsername.setForeground(Color.red);
            return;
        } else if (!matKhau.equals(acc.getPass())) {
            txtPass.setForeground(Color.RED);
            txtUsername.setForeground(Color.black);
            return;
        } else {
            txtUsername.setForeground(Color.black);
            txtPass.setForeground(Color.black);
            Auth.user = acc;
            this.dispose();
            FromMain mn = new FromMain();
            mn.setVisible(true);
        }
    }

